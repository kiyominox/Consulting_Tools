STEP19_NEG  = PatternFill('solid', fgColor='FCE4D6')
AUTO_FILL   = PatternFill('solid', fgColor='FFFACD')   # lemon yellow = auto-calculated

thin = Side(style='thin')
thick = Side(style='medium')
THIN_BORDER  = Border(left=thin,  right=thin,  top=thin,  bottom=thin)
THICK_BORDER = Border(left=thick, right=thick, top=thick, bottom=thick)


def _hdr(ws, row, col, value, bold=True, fill=None, align='left', size=11):
    c = ws.cell(row=row, column=col, value=value)
    c.font = Font(bold=bold, color='FFFFFF' if fill == HDR_FILL else '000000', size=size)
    if fill:
        c.fill = fill
    c.alignment = Alignment(horizontal=align, vertical='center', wrap_text=True)
    c.border = THIN_BORDER
    return c


def _val(ws, row, col, value, fmt=MONEY_FMT, fill=None, bold=False, align='right'):
    c = ws.cell(row=row, column=col, value=value)
    if fmt:
        c.number_format = fmt
    if fill:
        c.fill = fill
    c.font = Font(bold=bold, size=10)
    c.alignment = Alignment(horizontal=align, vertical='center')
    c.border = THIN_BORDER
    return c


def _lbl(ws, row, col, value, fill=None, bold=False, indent=0):
    c = ws.cell(row=row, column=col, value=value)
    c.font = Font(bold=bold, size=10)
    if fill:
        c.fill = fill
    c.alignment = Alignment(horizontal='left', vertical='center',
                             indent=indent, wrap_text=True)
    c.border = THIN_BORDER
    return c


def month_label(year: int, month: int) -> str:
    return date(year, month, 1).strftime('%b %Y')


