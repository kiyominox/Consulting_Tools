    parts_row('2.',  'Cores in Stock (New)  – from MGR',            calc['cores_new'])
    parts_row('3.',  'Cores in Stock (Used) – from physical recap',  calc['cores_used'])
    parts_row('4.',  'Bowman',                                       calc['bowman'])
    parts_row('5.',  'Normal / Scheduled Parts Returns (Acct 11933)', calc['returns'])
    parts_row('6.',  'Claims  – from MGR',                           calc['claims'])
    parts_row('7.',  'Open Repair Orders & Invoices (Fast Lane)')
    parts_row('',    '', 'Service: Repair Orders',   calc['ro_svc'],  fill=SUB_FILL)
    parts_row('',    '', 'Parts: Counter Tickets',    calc['ro_ctr'],  fill=SUB_FILL)

    # Step 8 total
    ws.merge_cells(f'B{r}:C{r}')
    _lbl(ws, r, 1, '8.', fill=STEP8_FILL, bold=True)
    _lbl(ws, r, 2, 'Total Parts Adjusted Inventory Value  (Steps 1 – 7)', fill=STEP8_FILL, bold=True)
    ws.cell(row=r, column=3).fill = STEP8_FILL
    _lbl(ws, r, 4, '=', fill=STEP8_FILL)
    _val(ws, r, 5, calc['step8'], fill=STEP8_FILL, bold=True)
