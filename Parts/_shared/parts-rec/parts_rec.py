def safe(val):
    try:
        return float(val) if not pd.isna(val) else 0.0
    except (TypeError, ValueError):
        return 0.0


def calculate_rec(row: dict, s1_total: float, s2_total: float) -> dict:
    """Build all reconciliation line items from input row + auto timing diffs."""

    # Parts side
    inventory  = safe(row.get('Inventory'))
    tires      = safe(row.get('Tires'))
    oil        = safe(row.get('Oil'))
    total_inv  = inventory + tires + oil
    paint      = safe(row.get('Paint_on_Mixer'))
    cores_new  = safe(row.get('Cores_New'))
    cores_used = safe(row.get('Cores_Used'))
    bowman     = safe(row.get('Bowman'))
    returns    = safe(row.get('Returns_11933'))
    claims     = safe(row.get('Claims'))
    ro_svc     = safe(row.get('Open_ROs_Service'))
    ro_ctr     = safe(row.get('Counter_Tickets_Parts'))
    open_ros   = ro_svc + ro_ctr

    step8 = total_inv + paint + cores_new + cores_used + bowman + returns + claims + open_ros

    # GL side
    gl_24200 = safe(row.get('GL_24200'))
    gl_24300 = safe(row.get('GL_24300'))
    gl_24401 = safe(row.get('GL_24401'))
    gl_other = safe(row.get('GL_Other'))
    gl_total = gl_24200 + gl_24300 + gl_24401 + gl_other

    mfg_slips = safe(row.get('Mfg_Packing_Slips'))
    prepaid   = safe(row.get('Prepaid_Special_Orders'))
    inv_adj   = safe(row.get('Inv_Adjustments_RPM'))
    oil_adj   = safe(row.get('Oil_Shop_Supply_Adj'))
    rad       = safe(row.get('Appreciation_Depreciation'))

    step17 = gl_total + mfg_slips + s2_total + prepaid - s1_total + inv_adj + oil_adj + rad
    step19 = step17 - step8
    step19_pct = step19 / gl_total if gl_total != 0 else None

    return dict(
        inventory=inventory, tires=tires, oil=oil, total_inv=total_inv,
        paint=paint, cores_new=cores_new, cores_used=cores_used, bowman=bowman,
        returns=returns, claims=claims, ro_svc=ro_svc, ro_ctr=ro_ctr, open_ros=open_ros,
        step8=step8,
        gl_24200=gl_24200, gl_24300=gl_24300, gl_24401=gl_24401,
        gl_other=gl_other, gl_total=gl_total,
        mfg_slips=mfg_slips, s2_total=s2_total, prepaid=prepaid,
        s1_total=s1_total, inv_adj=inv_adj, oil_adj=oil_adj, rad=rad,
        step17=step17, step8_ref=step8, step19=step19, step19_pct=step19_pct,
    )


