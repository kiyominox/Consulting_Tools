def run(gl_path: str, inputs_path: str, output_path: str):
    print(f'Reading GL Detail: {gl_path}')
    gl_df = pd.read_excel(gl_path, dtype={'Account': str, 'Journal': str})

    # Normalise column names (strip whitespace)
    gl_df.columns = [str(c).strip() for c in gl_df.columns]

    required = ['Acctg. Date', 'Amount $', 'Control']
    missing  = [c for c in required if c not in gl_df.columns]
    if missing:
        sys.exit(f'GL Detail is missing columns: {missing}\n'
                 f'Found columns: {list(gl_df.columns)}')

    # Fill missing Control2
    if 'Control2' not in gl_df.columns:
        gl_df['Control2'] = ''

    print(f'Reading Monthly Inputs: {inputs_path}')
    inp_df = pd.read_excel(inputs_path, sheet_name='Monthly Inputs', header=2,
                           skiprows=[3])  # row 3 = col names, row 4 = descriptions → skip
    inp_df.columns = [str(c).strip() for c in inp_df.columns]
    inp_df = inp_df.dropna(subset=['Month'])
    inp_df = inp_df[inp_df['Month'].astype(str).str.strip() != '']

    results = []
    for _, inp_row in inp_df.iterrows():
        month_str = str(inp_row['Month']).strip()
        try:
            rec_year, rec_month = _parse_month(month_str)
        except ValueError as e:
            print(f'  WARNING: skipping row – {e}')
            continue

        print(f'  Processing {month_label(rec_year, rec_month)} …', end='')
        s1_df, s2_df, rv_df = classify_gl(gl_df, rec_year, rec_month)
        s1_total = s1_df['Amount $'].sum() if not s1_df.empty else 0.0
        s2_total = s2_df['Amount $'].sum() if not s2_df.empty else 0.0
        print(f'  Section A (step 13): ${s1_total:,.2f}   Section B (step 11): ${s2_total:,.2f}'
              f'   Review: {len(rv_df)} entries')

        calc = calculate_rec(inp_row.to_dict(), s1_total, s2_total)
        results.append(dict(year=rec_year, month=rec_month, calc=calc,
                            s1_df=s1_df, s2_df=s2_df, rv_df=rv_df,
                            inputs=inp_row.to_dict()))

    if not results:
        sys.exit('No valid months found in inputs file.')

    print(f'\nWriting output: {output_path}')
    wb = openpyxl.Workbook()
    wb.remove(wb.active)   # remove default empty sheet

    write_summary_sheet(wb, results)
    for res in results:
        write_rec_sheet(wb, res['year'], res['month'], res['calc'],
                        res['s1_df'], res['s2_df'], res['rv_df'], res['inputs'])
    write_gl_analysis_sheet(wb, results)

    wb.save(output_path)
    print('Done.')

    # Print quick summary
    print('\n' + '─' * 70)
    print(f'{"Month":<12} {"Parts Adj":>14} {"GL Adj":>14} {"Variance $":>14} {"Variance %":>10}')
    print('─' * 70)
    for res in results:
        c = res['calc']
        pct = f"{c['step19_pct']:.1%}" if c['step19_pct'] is not None else 'n/a'
        print(f'{month_label(res["year"],res["month"]):<12} '
              f'{c["step8"]:>14,.2f} {c["step17"]:>14,.2f} '
              f'{c["step19"]:>14,.2f} {pct:>10}')
    print('─' * 70)


def _parse_month(s: str) -> tuple[int, int]:
    s = s.strip()
    for sep in ('-', '/'):
        if sep in s:
            parts = s.split(sep)
            return int(parts[0]), int(parts[1])
    raise ValueError(f'Cannot parse month "{s}" – expected YYYY-MM or YYYY/MM')


