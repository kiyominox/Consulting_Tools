    r += 2

    # ── TIMING DIFFERENCE DETAIL ──────────────────────────────────────────
    if not s1_df.empty or not s2_df.empty or not rv_df.empty:
        ws.merge_cells(f'A{r}:F{r}')
        c = ws.cell(row=r, column=1,
                    value='TIMING DIFFERENCE DETAIL  (from GL export  –  auto-calculated)')
        c.font = Font(bold=True, size=10, color='FFFFFF')
        c.fill = PatternFill('solid', fgColor='7B7B7B')
        c.alignment = Alignment(horizontal='left', vertical='center', indent=1)
        ws.row_dimensions[r].height = 16
        r += 1

        def _timing_section(df, heading, sign_note, row_fill):
            nonlocal r
            if df.empty:
                return
            ws.merge_cells(f'A{r}:F{r}')
            c = ws.cell(row=r, column=1, value=f'{heading}   ({sign_note})')
            c.font = Font(bold=True, size=10)
            c.fill = row_fill
            c.alignment = Alignment(horizontal='left', vertical='center', indent=1)
            ws.row_dimensions[r].height = 15
            r += 1

            # Column headers
            for col, hdr in enumerate(['Reference', 'Acctg. Date', 'Amount', 'Control',
                                        'Control2', 'Posting Description'], start=1):
                c = ws.cell(row=r, column=col, value=hdr)
                c.font = Font(bold=True, size=9)
                c.fill = PatternFill('solid', fgColor='D9D9D9')
