# Journal codes that represent inventory CONSUMPTION (sales, RO charges, counter invoices).
# These will never be timing differences for the inventory GL and are excluded from all
# timing-diff sections.  Standard CDK Drive journal codes:
#   60 = Service RO parts charges
#   61 = Warranty RO parts charges
#   62 = Counter/wholesale invoice parts charges
SALES_JOURNALS = {'60', '61', '62'}


def _has_nontrivial_control(row) -> bool:
    """Return True if either Control or Control2 contains a non-empty, non-trivial value."""
    for field in ('Control', 'Control2'):
        raw = row.get(field, '')
        try:
            if pd.isna(raw):
                continue
        except (TypeError, ValueError):
            pass
        v = str(raw).strip().upper()
        if v and v not in ('NONE', '0', 'N/A', '', 'NAN'):
            return True
    return False


def classify_gl(gl_df: pd.DataFrame, rec_year: int, rec_month: int):
    """
    Return (section1_df, section2_df, review_df) for a given reconciliation month.

    section1  – Posted in rec month, control = future month
                → "Parts in Accounting but not in Inventory (Not Received)"
                → SUBTRACT from GL side

    section2  – Posted AFTER rec month, control ≤ rec month
                → "In Physical, Not in GL" / Misc. Vendor Purchases
                → ADD to GL side

    review    – Posted AFTER rec month, has a non-trivial control value but it cannot
                be parsed as a month (e.g. a PO/invoice reference like B12331600, or
                a JE reference).  Excludes sales journal types (60/61/62) since those
                are inventory consumption entries and are never timing differences.
    """
    rec_start = date(rec_year, rec_month, 1)
    rec_end   = date(rec_year, rec_month, last_day(rec_year, rec_month))

    s1, s2, rv = [], [], []

    for _, row in gl_df.iterrows():
        raw_dt = row.get('Acctg. Date')
        if pd.isna(raw_dt):
            continue

        journal = str(row.get('Journal', '') or '').strip()
        if journal in SALES_JOURNALS:
            continue  # never a timing difference

        acctg_dt   = pd.Timestamp(raw_dt)
        acctg_date = acctg_dt.date()

        ym = get_control_ym(row, acctg_dt)

        if rec_start <= acctg_date <= rec_end:
            if ym and ym > (rec_year, rec_month):
                s1.append(row.to_dict())

        elif acctg_date > rec_end:
            if ym and ym <= (rec_year, rec_month):
                s2.append(row.to_dict())
            elif ym is None and _has_nontrivial_control(row):
                # Non-month reference in a purchase/adjustment journal — flag for review
                rv.append(row.to_dict())

    empty = pd.DataFrame(columns=gl_df.columns)
    s1_df = pd.DataFrame(s1) if s1 else empty.copy()
    s2_df = pd.DataFrame(s2) if s2 else empty.copy()
    rv_df = pd.DataFrame(rv) if rv else empty.copy()
    return s1_df, s2_df, rv_df


