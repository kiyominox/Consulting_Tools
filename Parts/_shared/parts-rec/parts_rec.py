MONTH_MAP = {
    'JAN': 1, 'FEB': 2, 'MAR': 3, 'APR': 4, 'MAY': 5, 'JUN': 6,
    'JUL': 7, 'AUG': 8, 'SEP': 9, 'SEPT': 9, 'OCT': 10, 'NOV': 11, 'DEC': 12,
    'JANUARY': 1, 'FEBRUARY': 2, 'MARCH': 3, 'APRIL': 4, 'JUNE': 6,
    'JULY': 7, 'AUGUST': 8, 'SEPTEMBER': 9, 'OCTOBER': 10,
    'NOVEMBER': 11, 'DECEMBER': 12,
}

# Build a list sorted longest-first so "SEPT" matches before "SEP"
_ABBR_LIST = sorted(MONTH_MAP.keys(), key=len, reverse=True)


def _infer_year(control_month: int, acctg_dt) -> int | None:
    """
    Given a bare month number (no year) and an accounting date, return the
    most likely year.  Searches ±1 year from acctg_dt.year and picks the
    candidate that falls within −18 months to +2 months of acctg_dt,
    choosing the one closest in time.
    """
    ref = date(acctg_dt.year, acctg_dt.month, 1)
    best_year = None
    best_diff = float('inf')
    for offset in (-1, 0, 1):
        yr = acctg_dt.year + offset
        try:
            cand = date(yr, control_month, 1)
        except ValueError:
            continue
        delta = (cand - ref).days
        if -550 <= delta <= 62:          # ~18 months back, ~2 months forward
            if abs(delta) < best_diff:
                best_diff = abs(delta)
                best_year = yr
    return best_year


def parse_control_month(control_val, acctg_dt) -> tuple[int, int] | None:
    """
    Parse a single control/control2 value into (year, month) or None.

    Handles:
      - Bare month names:  NOV, DEC, SEPT, MARCH, …
      - Month + 2-digit year: JAN25, NOV24, SEPT24
      - Month + 4-digit year: JAN2025
    Returns None for invoice references, blank values, etc.
    """
    if not control_val:
        return None
    s = str(control_val).strip().upper()
    if s in ('NONE', '0', '', 'N/A'):
        return None

    for abbr in _ABBR_LIST:
        if not s.startswith(abbr):
            continue
        rest = s[len(abbr):]
        month_num = MONTH_MAP[abbr]

        if rest == '':
            # Bare month name
            yr = _infer_year(month_num, acctg_dt)
            return (yr, month_num) if yr else None

        if rest.isdigit():
            if len(rest) == 2:
                return (2000 + int(rest), month_num)
            if len(rest) == 4:
                return (int(rest), month_num)

        # e.g. "MARCH2025" already matched via "MARCH" above
        # If rest is non-numeric the prefix match was spurious (e.g. "B13…")
        break

    return None


def get_control_ym(row, acctg_dt) -> tuple[int, int] | None:
    """Try Control then Control2 for a month reference."""
    ym = parse_control_month(row.get('Control'), acctg_dt)
    if ym is None:
        ym = parse_control_month(row.get('Control2'), acctg_dt)
    return ym


def last_day(year: int, month: int) -> int:
    return calendar.monthrange(year, month)[1]


