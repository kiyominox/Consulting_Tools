MONEY_FMT   = '#,##0.00'
PCT_FMT     = '0.00%'
HDR_FILL    = PatternFill('solid', fgColor='1F4E79')   # dark blue
PARTS_FILL  = PatternFill('solid', fgColor='D6E4F0')   # light blue
GL_FILL     = PatternFill('solid', fgColor='E2EFDA')   # light green
VAR_POS     = PatternFill('solid', fgColor='FCE4D6')   # light red (GL > Parts)
VAR_NEG     = PatternFill('solid', fgColor='FCE4D6')
VAR_ZERO    = PatternFill('solid', fgColor='E2EFDA')   # green when close
SUB_FILL    = PatternFill('solid', fgColor='F2F2F2')
STEP8_FILL  = PatternFill('solid', fgColor='BDD7EE')   # medium blue
STEP17_FILL = PatternFill('solid', fgColor='A9D18E')   # medium green
