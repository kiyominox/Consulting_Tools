    step8_row = r
    r += 2

    # ── GL SECTION HEADER ─────────────────────────────────────────────────
    ws.merge_cells(f'A{r}:F{r}')
    c = ws.cell(row=r, column=1, value='ACCOUNTING  (General Ledger)')
